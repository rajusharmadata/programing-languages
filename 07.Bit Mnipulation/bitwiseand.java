public class bitwiseand {

    public static void main(String[] args) {
        // bitwise and (&)
        System.out.println(5 & 6);
        // output - 4 

        // oR operatior 
        System.out.println(5 | 6);
        // output - 7

        //  XOR ^ 
        System.out.println( 5^6);
        // output 3
        // one's complement 
        System.out.println(~5);
        // -6
        System.out.println(~0);
        // outptu -1
        // left shift 
        System.out.println(5<<2);
        // output 20
        // Right shift 
        System.out.println(6>>1);
        // 3
    }
}